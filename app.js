class Fragment {

 constructor() {
   this.fragment = document.createDocumentFragment();
 }

 #addHeader(name) {
   const header = document.createElement('h1');
   header.innerHTML = name;
   this.fragment.appendChild(header);
 }

 #createEl(el) {
   return document.createElement(el);
 }

 #addItem(item) {
   // Create figure
   const figure = this.#createEl('figure');
   figure.innerHTML = `
    <img src=${item.image}>
   `
   let figcaption = this.#createEl('figcaption');
   figure.appendChild(figcaption);
   figcaption.innerHTML = `<a target="_blank" href=${item.link}><b>${item.company} | ${item.name}</b></a></br>`

   const attributes = item.attributes;
   for (let attribute of Object.keys(attributes)) {
     figcaption.innerHTML += `${attributes[attribute]}</br>`
   }
   return figure;
 }

 addSection(sectionObj, name) {
   this.#addHeader(name);
   const sectionEl = document.createElement('section');
   sectionObj.forEach(item => {
     const figure = this.#addItem(item);
     sectionEl.appendChild(figure);
   });
   this.fragment.appendChild(sectionEl);
 }

 populateDOM() {
   document.body.appendChild(this.fragment);
 }
}

